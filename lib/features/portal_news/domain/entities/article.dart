class Article {
  final String id;
  final String autor;
  final String title;
  final String content;
  final String description;
  final String imageUrl;
  final String updatedAt;

  Article({
    required this.id,
    required this.autor,
    required this.title,
    required this.content,
    required this.description,
    required this.imageUrl,
    required this.updatedAt,
  });
}
